#include "libft.h"

char	*ft_str_tolower(char *s)
{
	int	i;

	if (!s)
		return (NULL);
	i = -1;
	while (s[++i])
		if (s[i] >= 65 && s[i] <= 90)
			s[i] += 32;
	return (s);
}
