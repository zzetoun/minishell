#include "libft.h"

int	ft_isalpha(int c)
{
	char	i;

	i = 65;
	while (i <= 90)
	{
		if (i == c)
			return (1);
		i++;
	}
	i = 97;
	while (i <= 122)
	{
		if (i == c)
			return (1);
		i++;
	}
	return (0);
}
